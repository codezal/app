// Built-in theme presets. Each preset declares every CSS token used by the app.
// Token values are HSL components ("H S% L%") so they compose with hsl(var(--token) / alpha).
// lineRgb is "R G B" (decimal 0-255 triplets) for rgb(var(--codezal-line-rgb) / alpha).

export type ThemeMode = "light" | "dark"

export type ThemeTokens = {
  // Shadcn base
  background: string
  foreground: string
  card: string
  cardForeground: string
  popover: string
  popoverForeground: string
  primary: string
  primaryForeground: string
  secondary: string
  secondaryForeground: string
  muted: string
  mutedForeground: string
  accent: string
  accentForeground: string
  destructive: string
  destructiveForeground: string
  border: string
  input: string
  ring: string
  // Codezal surfaces
  codezalBg: string
  codezalSidebar: string
  codezalPanel: string
  codezalPanel2: string
  codezalTitleBar: string
  codezalInput: string
  codezalChip: string
  codezalCodeBg: string
  codezalCodeChip: string
  // Text
  codezalText: string
  codezalTextDim: string
  codezalTextMute: string
  // Lines
  codezalBorder: string
  codezalHairline: string
  codezalLineRgb: string
  // Accents
  codezalAccent: string
  codezalAccentDim: string
  codezalOk: string
  codezalDiffAdd: string
  codezalDiffDel: string
}

export type ThemePreset = {
  id: string
  name: string
  mode: ThemeMode
  tokens: ThemeTokens
  builtin?: boolean
}

